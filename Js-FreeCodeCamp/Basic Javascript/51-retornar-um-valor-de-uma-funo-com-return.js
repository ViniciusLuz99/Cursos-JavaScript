// Define uma função chamada "timesFive" que recebe um parâmetro chamado "num".
function timesFive(num) {
  // Multiplica o parâmetro "num" por 5.
  return num * 5;
}

// Chama a função "timesFive" passando o número 2 como argumento
// e armazena o resultado na variável "answer".
const answer = timesFive(2);

// Chama a função "timesFive" passando o número 5 como argumento
// e armazena o resultado na variável "answer2".
