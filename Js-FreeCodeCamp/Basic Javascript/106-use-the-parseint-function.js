// Função para converter uma string em um valor inteiro (número inteiro).
// Recebe uma string 'str' como argumento a ser convertida em um número inteiro.
function convertToInteger(str) {
  // A função parseInt() é usada para converter a string em um valor inteiro (número inteiro).
  // A variável 'converter' armazena o resultado da conversão.
  let converter = parseInt(str);

  // Retorna o valor inteiro resultante da conversão.
  return converter;
}

convertToInteger("56");